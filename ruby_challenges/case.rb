  puts "Sky's out thighs out!"
when 'cloudy'
  puts "Perhaps a light jacket will do"
when 'rainy'
  puts "Grab your raincoat!"
when 'hot'
  puts "Turn up the A/C"
when 'foggy'
  puts "What is this, San Francisco?"
else
  puts "Who knows!"
end #end the case statement with the end keyword
